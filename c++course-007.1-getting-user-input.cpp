// Bibliotecas
#include <iostream>
#include <cmath>

using namespace std;

// Getting user input

int main()
{
    // Criando a variável
    int age;

    // Pedindo a informação para o usuário
    cout << "Enter your age: ";

    // Armazenando a informação
    cin >> age;

    // Retornando a informação para o usuário

    cout << "You're " << age << " years old!";

    return 0;
}

